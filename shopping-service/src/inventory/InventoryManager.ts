  async bulkImport(
    userId: string,
    data: Array<{
      name: string;
      brand?: string;
      category: string;
      color: string;
      size: string;
      price?: number;
      barcode?: string;
      location?: string;
      [key: string]: any;
    }>,
    format: 'csv' | 'xlsx' | 'json'
  ): Promise<BulkImportResult> {
    const result: BulkImportResult = {
      successful: 0,
      failed: 0,
      duplicates: 0,
      errors: [],
      imported: []
    };

    for (let i = 0; i < data.length; i++) {
      try {
        const itemData = data[i];
        const existingItem = await this.findDuplicateItem(userId, itemData);

        if (existingItem) {
          result.duplicates++;
          continue;
        }

        const item = await this.createItemFromImport(itemData);
        const entry = await this.addItem(userId, item, this.parseLocation(itemData.location), {
          generateBarcode: !itemData.barcode,
          autoValuation: true
        });

        result.successful++;
        result.imported.push(item.id);

      } catch (error) {
        result.failed++;
        result.errors.push({
          row: i + 1,
          item: data[i],
          error: error instanceof Error ? error.message : 'Unknown error'
        });
      }
    }

    return result;
  }

  async exportInventory(
    userId: string,
    format: 'csv' | 'xlsx' | 'pdf' | 'json',
    options: {
      includePhotos?: boolean;
      includeValuation?: boolean;
      groupBy?: 'category' | 'location' | 'brand' | 'season';
      filter?: Record<string, any>;
    } = {}
  ): Promise<{
    data: ArrayBuffer | string;
    filename: string;
    metadata: {
      totalItems: number;
      totalValue: number;
      exportDate: Date;
      filters: any;
    };
  }> {
    const inventory = await this.getUserInventory(userId);
    const filteredItems = this.applyFilters(inventory, options.filter);

    const metadata = {
      totalItems: filteredItems.length,
      totalValue: filteredItems.reduce((sum, item) => sum + (item.valuation.currentValue || 0), 0),
      exportDate: new Date(),
      filters: options.filter || {}
    };

    let data: ArrayBuffer | string;
    let filename: string;

    switch (format) {
      case 'csv':
        data = this.exportToCSV(filteredItems, options);
        filename = `inventory_${new Date().toISOString().split('T')[0]}.csv`;
        break;
      case 'xlsx':
        data = await this.exportToExcel(filteredItems, options);
        filename = `inventory_${new Date().toISOString().split('T')[0]}.xlsx`;
        break;
      case 'pdf':
        data = await this.exportToPDF(filteredItems, options);
        filename = `inventory_${new Date().toISOString().split('T')[0]}.pdf`;
        break;
      case 'json':
        data = JSON.stringify(filteredItems, null, 2);
        filename = `inventory_${new Date().toISOString().split('T')[0]}.json`;
        break;
      default:
        throw new Error('Invalid export format');
    }

    return { data, filename, metadata };
  }

