  private async calculateCurrentValue(item: IClothingItem): Promise<number> {
    const originalPrice = item.purchase.price || 0;
    const ageInMonths = Math.floor((Date.now() - (item.purchase.date?.getTime() || Date.now())) / (30 * 86400000));
    const wearCount = item.metadata.wearCount || 0;

    let depreciationRate = 0.1;

    if (item.category.formality === 'formal') depreciationRate = 0.05;
    if (item.materials.primary.includes('leather')) depreciationRate = 0.08;
    if (['luxury', 'designer'].some(tag => item.tags.includes(tag))) depreciationRate = 0.06;

    const ageDepreciation = Math.min(ageInMonths * 0.02, 0.5);
    const wearDepreciation = Math.min(wearCount * 0.01, 0.3);

    const currentValue = originalPrice * (1 - depreciationRate - ageDepreciation - wearDepreciation);
    return Math.max(currentValue, originalPrice * 0.1);
  }

