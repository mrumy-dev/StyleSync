  async addItem(
    userId: string,
    item: IClothingItem,
    location: {
      closetId: string;
      spaceId: string;
      sectionId: string;
      position?: { x: number; y: number; z: number };
    },
    options: {
      generateBarcode?: boolean;
      assignRFID?: boolean;
      autoValuation?: boolean;
    } = {}
  ): Promise<InventoryEntry> {
    const entry: InventoryEntry = {
      id: `inv_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`,
      itemId: item.id,
      location: {
        ...location,
        position: location.position || { x: 0, y: 0, z: 0 }
      },
      status: 'available',
      lastSeen: new Date(),
      checkInHistory: [{
        timestamp: new Date(),
        method: 'manual',
        location: `${location.closetId}/${location.spaceId}/${location.sectionId}`,
        confidence: 1.0
      }],
      valuation: {
        purchasePrice: item.purchase.price,
        currentValue: options.autoValuation ? await this.calculateCurrentValue(item) : item.purchase.price
      },
      metadata: {
        addedAt: new Date(),
        lastUpdated: new Date(),
        source: 'manual'
      }
    };

    if (options.generateBarcode) {
      entry.barcode = this.generateBarcode(item);
    }

    if (options.assignRFID) {
      entry.rfidTag = await this.assignRFIDTag(item.id);
    }

    await this.cacheInventoryEntry(userId, entry);
    return entry;
  }

  async scanBarcode(code: string): Promise<{
    item?: IClothingItem;
    productData?: any;
    suggestions?: any[];
  }> {
    const barcodeData = await this.decodeBarcodeData(code);
    this.barcodeCache.set(code, barcodeData);

    const existingItem = await this.findItemByBarcode(code);
    if (existingItem) {
      return { item: existingItem };
    }

    const productData = await this.lookupProductData(barcodeData);
    if (productData) {
      return { productData };
    }

    const suggestions = await this.generateItemSuggestions(barcodeData);
    return { suggestions };
  }

  async readRFIDTag(tagId: string): Promise<{
    item?: IClothingItem;
    location?: string;
    lastRead?: Date;
  }> {
    const rfidData = await this.readRFIDData(tagId);
    this.rfidCache.set(tagId, rfidData);

    const item = await this.findItemByRFID(tagId);
    if (item) {
      await this.updateItemLocation(item.id, rfidData.data.location);
      return {
        item,
        location: rfidData.data.location,
        lastRead: rfidData.lastRead
      };
    }

    return {};
  }

